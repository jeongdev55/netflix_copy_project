
$('.user_content1').click(function(){
    if($('.click_div1').css("display")=="none"){
        $('#down_btn').attr("src","up_icon.png");
        $('.click_div1').css("display","block");
    }else{
        $('#down_btn').attr("src","down2_icon.png");
        $('.click_div1').css("display","none");
    }
});

